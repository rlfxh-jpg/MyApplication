package com.example.myapplication.logic.model;

public class WhiteNoise {
    int id;
    String name;

    public WhiteNoise(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
